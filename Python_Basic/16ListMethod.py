userIdentity = ["Divo", "Daniel", "Saputra"]
print(userIdentity)
print("\n")

#! method append
userIdentity.append("17 Tahun") #? append digunakan untuk menambahkan data baru yang terletak di dibelakang list
print(userIdentity)
print("\n")

#! method insert
userIdentity.insert(1, 100) #? Sama aja kayak append, bedanya kita bebas naruh index-nya dimana
print(userIdentity)
print("\n")

#! method pop
userIdentity.pop(0)
print(userIdentity)
print("\n")

#! method remove
userIdentity.remove("Daniel") #? Sama aja kayak pop, bedanya pop pake urutan index kalo ini langsung
print(userIdentity)
print("\n")

